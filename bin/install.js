async function setupVPSDeployment(installPath) {
  log("Setting up VPS deployment template...");

  console.log("\n🎉 VPS template ready!");
  console.log("======================");
  console.log("For VPS deployment, follow this guide:");
  console.log("📖 docs/vps-deployment.md");
  console.log("\nQuick VPS setup:");
  console.log("1. Get a VPS (DigitalOcean, Linode, Vultr)");
  console.log("2. Run the Ubuntu setup commands");
  console.log("3. Deploy your Krab instance");
  console.log("4. Set up domain and SSL");
}

async function main() {
  try {
    await checkPrerequisites();

    const installPath = await getInstallationPath();
    const deploymentType = await selectDeploymentType();
    const provider = await configureEnvironment(installPath);

    await installKrab(installPath, deploymentType);
    await setupDeployment(installPath, deploymentType, provider);

    log("\n🎊 Krab installation completed successfully!", "success");
    log("Happy chatting with Krab! 🦀", "success");

  } catch (error) {
    log(`Installation failed: ${error.message}`, "error");
    process.exit(1);
  } finally {
    rl.close();
  }
}

// Handle different invocation methods
if (import.meta.url === `file://${process.argv[1]}`) {
  main();
}

export { main as installKrab };
